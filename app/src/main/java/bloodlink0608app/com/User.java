package bloodlink0608app.com;

public class User {
    public String village, bloodGroup, district, dateofbirth, email, facebookid, gender, phone1,
            phone2, postcode, uid, username, weight, image, lastdate,status;

    public User() {
    }

    public User(String village, String bloodGroup, String district, String dateofbirth, String email, String facebookid, String gender, String phone1, String phone2, String postcode, String uid, String username, String weight, String image, String lastdate, String status) {
        this.village = village;
        this.bloodGroup = bloodGroup;
        this.district = district;
        this.dateofbirth = dateofbirth;
        this.email = email;
        this.facebookid = facebookid;
        this.gender = gender;
        this.phone1 = phone1;
        this.phone2 = phone2;
        this.postcode = postcode;
        this.uid = uid;
        this.username = username;
        this.weight = weight;
        this.image = image;
        this.lastdate = lastdate;
        this.status = status;
    }

    public String getVillage() {
        return village;
    }

    public void setVillage(String village) {
        this.village = village;
    }

    public String getBloodGroup() {
        return bloodGroup;
    }

    public void setBloodGroup(String bloodGroup) {
        this.bloodGroup = bloodGroup;
    }

    public String getDistrict() {
        return district;
    }

    public void setDistrict(String district) {
        this.district = district;
    }

    public String getDateofbirth() {
        return dateofbirth;
    }

    public void setDateofbirth(String dateofbirth) {
        this.dateofbirth = dateofbirth;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getFacebookid() {
        return facebookid;
    }

    public void setFacebookid(String facebookid) {
        this.facebookid = facebookid;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getPhone1() {
        return phone1;
    }

    public void setPhone1(String phone1) {
        this.phone1 = phone1;
    }

    public String getPhone2() {
        return phone2;
    }

    public void setPhone2(String phone2) {
        this.phone2 = phone2;
    }

    public String getPostcode() {
        return postcode;
    }

    public void setPostcode(String postcode) {
        this.postcode = postcode;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getWeight() {
        return weight;
    }

    public void setWeight(String weight) {
        this.weight = weight;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getLastdate() {
        return lastdate;
    }

    public void setLastdate(String lastdate) {
        this.lastdate = lastdate;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
}
